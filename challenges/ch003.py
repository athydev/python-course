var = input('Digite algo: ')

print(f'isalnum: {var.isalnum()} \n'
      f'isalpha: {var.isalpha()} \n'
      f'isascii: {var.isascii()} \n'
      f'isdigit: {var.isdigit()} \n'
      f'islower: {var.islower()} \n'
      f'isdecimal: {var.isdecimal()} \n'
      f'isidentifier: {var.isidentifier()} \n'
      f'isnumeric: {var.isnumeric()} \n'
      f'isprintable: {var.isprintable()} \n'
      f'isspace: {var.isspace()} \n'
      f'istitle: {var.istitle()} \n'
      f'isupper: {var.isupper()} \n')
